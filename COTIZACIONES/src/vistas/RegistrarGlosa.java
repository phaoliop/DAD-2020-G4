    //------------------------- Procesar ---------------------------------------------------------    
    private void procesar(int op) {
       glosa=leerDatos();
        try {
            String msg=glosacontroler.GlosaProcesar(glosa, op);
            JOptionPane.showMessageDialog(null, msg);            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e.getMessage());
             System.out.println("Error"+e.getMessage());
        }       
   }
    
   private Glosa leerDatos() {
        Glosa glos = new Glosa();
        
        glos.setIdGlosa(idGlosa);
        glos.setConcepto(txtConcepto.getText());
        glos.setCodigo(txtCodigo.getText());
        
        return glos;
    }

   // ------------------------------ Listar Tabla ----------------------------------------
    
    void listaGlosa() {
        List<Glosa> lista;
        try {
            
            lista = glosacontroler.GlosaListar();
            verGlosa(lista);

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("ERROR: " + e.getMessage());
        }
    }

     private void verGlosa(List<Glosa> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (Glosa glo : lista) {
            Object[] fila = {glo.getIdGlosa(), glo.getConcepto(), glo.getCodigo()};
            tabla.addRow(fila);
        }
    }
     
   //------------------------ Consulta ------------------------------------
     private void consultar(int id) throws Exception {
      glosa = glosacontroler.GlosaBuscar(id);
        if ( glosa!= null) {

            labelIdGlosa.setText(glosa.getIdGlosa()+"");
            idGlosa=glosa.getIdGlosa();
            txtConcepto.setText(glosa.getConcepto());
            txtCodigo.setText(glosa.getCodigo());
                                   
        } else {
            JOptionPane.showMessageDialog(null, "Concepto de Pago no registrado");
            //System.out.println("Error");
        }
    }
