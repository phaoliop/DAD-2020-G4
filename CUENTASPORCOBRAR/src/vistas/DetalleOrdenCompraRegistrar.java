    private void buttonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Se eliminará el registro, ¿desea continuar?",
                "Eliminar Registro", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

            procesar(3);
            OrdenesCompra.listaDetalleOrdenCompra(idOrdenCompra);
            try {
                OrdenesCompra.consultarPago(idOrdenCompra);
            } catch (Exception ex) {
                Logger.getLogger(DetalleOrdenCompraRegistrar.class.getName()).log(Level.SEVERE, null, ex);
            }
            limpiar();
            habilitarEliminar();
        }
        dispose();
    }//GEN-LAST:event_buttonEliminarActionPerformed

