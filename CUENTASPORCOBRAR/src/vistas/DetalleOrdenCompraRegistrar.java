// ---------------------------- Procesar --------------------------------    
    private void procesar(int op) {
        detalleordencompra = leerDatos();
        try {
            String msg = detalleordencompracontroler.DetalleOrdenCompraProcesar(detalleordencompra, op);
            JOptionPane.showMessageDialog(null, msg);
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("Error" + e.getMessage());
        }
    }

// -----------------------------Leer Datos ----------------------------------        
    private DetalleOrdenCompra leerDatos() {

        DetalleOrdenCompra detOrdC = new DetalleOrdenCompra();

        detOrdC.setDoc_id(idDetalleOrdenCompra);
        detOrdC.setDoc_idOrdenComprafk(idOrdenCompra);
        detOrdC.setDoc_item(txtItem.getText());
        detOrdC.setDoc_cantidad(txtCantidad.getText());
        detOrdC.setDoc_desc(txtDescripcion.getText());
        detOrdC.setDoc_detalleDesc(txtDetalleDescripcion.getText());
        detOrdC.setDoc_precioUnit(txtPrecioUnit.getText());

        return detOrdC;
    }

//------------------------------- Consutar detalle ------------------------------
    static void consultar(int idDetalleordco) throws Exception {

        detalleordencompra = detalleordencompracontroler.DetalleOrdenCompraBuscar(idDetalleordco);
        if (detalleordencompra != null) {

            idDetalleOrdenCompra = detalleordencompra.getDoc_id();
            OrdenesCompra.idDetalleOrdenCompra = detalleordencompra.getDoc_id();
            idOrdenCompra = detalleordencompra.getDoc_idOrdenComprafk();
            OrdenesCompra.idOrdenCompraParaDetalle = detalleordencompra.getDoc_idOrdenComprafk();
            txtItem.setText(detalleordencompra.getDoc_item());
            txtCantidad.setText(detalleordencompra.getDoc_cantidad());
            txtDescripcion.setText(detalleordencompra.getDoc_desc());
            txtDetalleDescripcion.setText(detalleordencompra.getDoc_detalleDesc());
            txtPrecioUnit.setText(detalleordencompra.getDoc_precioUnit());

        } else {
            JOptionPane.showMessageDialog(null, "Detalle no registrado");
            //System.out.println("Error");
        }
    }
    
