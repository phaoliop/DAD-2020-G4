//------------------------------ PROCESAR --------------------------------------

    private void procesar(int op) {
        transportista = leerDatos();
        try {
            String msg = transportistacontroler.TransportistaProcesar(transportista, op);
            JOptionPane.showMessageDialog(null, msg);
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("Error" + e.getMessage());
        }
    }

    private Transportista leerDatos() {
        Transportista transp = new Transportista();

        
        transp.setNombre(txtNombreTransportista.getText());
        transp.setRuc(txtRuc.getText());
        transp.setMarcaYPlaca(txtMarcaPlaca.getText());
        transp.setCertificadoInscripcion(txtCertificadoInscripcion.getText());
        transp.setLicenciaConducir(txtLicenciaConducir.getText());
        transp.setIdTransportista(Integer.parseInt(labelIdTransportista.getText()));
        
        return transp;
    }
    
//-------------------------------- CONSULTAR -----------------------------------
    
     private void consultar(int id) throws Exception {
       transportista = transportistacontroler.TransportistaBuscar(id);
        if (transportista!= null) {
            
            idTransportista=transportista.getIdTransportista();
            labelIdTransportista.setText(Integer.toString(transportista.getIdTransportista()));
            txtNombreTransportista.setText(transportista.getNombre());
            txtRuc.setText(transportista.getRuc());
            txtMarcaPlaca.setText(transportista.getMarcaYPlaca());
            txtCertificadoInscripcion.setText(transportista.getCertificadoInscripcion());
            txtLicenciaConducir.setText(transportista.getLicenciaConducir());
                       
        } else {
            JOptionPane.showMessageDialog(null, "Articulo no registrado");
            //System.out.println("Error");
        }
    }

//---------------------------------- TABLA -------------------------------------
    void listaTransportista() {
        List<Transportista> lista;
        try {

            lista = transportistacontroler.TransportistaListar();
            verTransportista(lista);

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("ERROR: " + e.getMessage());
        }
    }

    private void verTransportista(List<Transportista> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (Transportista trans : lista) {
            Object[] fila = {trans.getIdTransportista(), trans.getNombre(), trans.getRuc() ,trans.getMarcaYPlaca(),
                trans.getLicenciaConducir()};
            tabla.addRow(fila);
        }
    }
    
