    private void buttonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Se eliminará el registro, ¿desea continuar?",
                "Eliminar Registro", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            procesar(3);
            listaTipoCambio();
            limpiarCampos();
            habilitarEliminar();
            try {
                consultarUltimaFecha();
            } catch (Exception ex) {
                Logger.getLogger(TiposDeCambio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonEliminarActionPerformed

    private void buttonNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonNuevoActionPerformed

        if (txtFecha.getText()== null ? fechaDia == null : txtFecha.getText().equals(fechaDia)) {
            JOptionPane.showMessageDialog(null, "Tipo de cambio ya registrado");
            habilitarInicio();

        } else {
            limpiarCampos();
            habilitarNuevo();
        }
    }//GEN-LAST:event_buttonNuevoActionPerformed


