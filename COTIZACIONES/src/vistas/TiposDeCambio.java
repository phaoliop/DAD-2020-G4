//-------------------------- Consultar -----------------------------------------
    private void consultar() throws Exception {
        pro = obj.TipoCambioBuscar(fecha);
        if (pro != null) {

            idTipo = pro.getIdTipoCambio();
            txtFecha.setText(pro.getFecha());
            txtPrecioCompra.setText(Float.toString(pro.getPrecioCompra()));
            txtPrecioVenta.setText(Float.toString(pro.getPrecioVenta()));

        } else {
            JOptionPane.showMessageDialog(null, "Tipo de cambio no registrado");
            //System.out.println("Error");
        }
    }

    private void consultarUltimaFecha() throws Exception {
        pro = obj.TipoCambioBuscarUltimaFecha();
        if (pro != null) {
            fechaDia = pro.getFecha();
        }
    }

// -------------------------- Procesar ------------------------------------------------------
    private void procesar(int op) {
        pro = leerDatos();
        try {
            System.out.println("11111111111");
            String msg = obj.TipoCambioProcesar(pro, op);
            JOptionPane.showMessageDialog(null, msg);
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("Error" + e.getMessage());
        }
    }

//------------------------------ Leer Datos-------------------------------------
    private TipoCambio leerDatos() {
        TipoCambio tipoCambio = new TipoCambio();

        tipoCambio.setIdTipoCambio(idTipo);
        tipoCambio.setFecha(txtFecha.getText());
        tipoCambio.setPrecioCompra(Float.parseFloat(txtPrecioCompra.getText()));
        tipoCambio.setPrecioVenta(Float.parseFloat(txtPrecioVenta.getText()));

        return tipoCambio;
    }

    //-------------------------------- Tabla --------------------------------------
    static void listaTipoCambio() {
        List<TipoCambio> lista;
        try {
            lista = obj.TipoCambioListar();
            verDetalleOrdenCorte(lista);

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("ERROR: " + e.getMessage());
        }
    }

    static private void verDetalleOrdenCorte(List<TipoCambio> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (TipoCambio tipCam : lista) {
            Object[] fila = {tipCam.getFecha(), tipCam.getPrecioCompra(), tipCam.getPrecioVenta()};
            tabla.addRow(fila);

        }
    }

    // ------------------------ Fecha --> Indica la fecha del dia --------------------
    public String fechaActual() {
        Date fecha = new Date();

        SimpleDateFormat formatofecha = new SimpleDateFormat("YYYY-MM-dd");
        return formatofecha.format(fecha);
    }

    // ------------------------ Limpiar -----------------------------------
    public void limpiarCampos() {
        txtPrecioCompra.setText("");
        txtPrecioVenta.setText("");
        txtFecha.setText(fechaActual());
    }

