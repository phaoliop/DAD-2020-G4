//---------------------------------- Procesar ---------------------------------------
    
     private void procesar(int op) {
       sucursal=leerDatos();
        try {
            String msg=sucursalcontroler.SucursalProcesar(sucursal, op);
            JOptionPane.showMessageDialog(null, msg);            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e.getMessage());
             System.out.println("Error"+e.getMessage());
        }       
   }
    
   private Sucursal leerDatos() {
        Sucursal sucu = new Sucursal();
        
        sucu.setIdSucursal(idSucursal);
        sucu.setSucursal(txtSucursal.getText());
        sucu.setClave(txtNombreReducido.getText());
       
        return sucu;
    }
   
   //------------------------ Consulta ------------------------------------
     private void consultar(int id) throws Exception {
      sucursal = sucursalcontroler.SucursalBuscar(id);
        if ( sucursal!= null) {

            labelIdSucursal.setText(sucursal.getIdSucursal()+"");
            idSucursal=sucursal.getIdSucursal();
            txtSucursal.setText(sucursal.getSucursal());
            txtNombreReducido.setText(sucursal.getClave());
            
                                   
        } else {
            JOptionPane.showMessageDialog(null, "Sucursal no registrado");
            //System.out.println("Error");
        }
    }
   
   
   
   // ------------------------------ Listar Tabla ----------------------------------------
    
    void listaSucursal() {
        List<Sucursal> lista;
        try {
            
            lista = sucursalcontroler.SucursalListar();
            verRubro(lista);

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("ERROR: " + e.getMessage());
        }
    }

     private void verRubro(List<Sucursal> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (Sucursal sucu : lista) {
            Object[] fila = {sucu.getIdSucursal(), sucu.getSucursal(), sucu.getClave()};
            tabla.addRow(fila);
        }
    }

