    private void verContactoCliente(List<ContactoCliente> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (ContactoCliente conCli : lista) {
            Object[] fila = {conCli.getIdContactoCliente(),conCli.getDni(), conCli.getNombres(),conCli.getApellidos(),conCli.getCargo(),conCli.getTlf1(),conCli.getTlf2(),conCli.getCorreo(),conCli.getSucursal(),conCli.getObservacion()};
            tabla.addRow(fila);
        }
    }
