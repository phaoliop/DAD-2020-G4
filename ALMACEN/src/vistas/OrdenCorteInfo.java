    static private void verDetalleOrdenCorte(List<DetalleOrdenCorte> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (DetalleOrdenCorte detOrd : lista) {
            Object[] fila = {detOrd.getCodArt(), detOrd.getProcedencia(), detOrd.getCantidad(), detOrd.getDiametro(), detOrd.getLongitud(), a, detOrd.getCodUbi(), detOrd.getLongReal()};
            tabla.addRow(fila);

        }
    }


