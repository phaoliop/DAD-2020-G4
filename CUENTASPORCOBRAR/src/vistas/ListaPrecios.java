    private void listaListaPrecio() {
        List<ListaPrecio> lista;
        try {
            lista = obj.ListaPrecioListar();
            verListaPrecio(lista);

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("ERROR: " + e.getMessage());
        }
    }

    private void verListaPrecio(List<ListaPrecio> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (ListaPrecio listPre : lista) {
            Object[] fila = {listPre.getNombre(),listPre.getDiametro(), listPre.getUnidadDiametro(), listPre.getPrecio(), listPre.getTipoCod()};
            tabla.addRow(fila);
        }
    }

