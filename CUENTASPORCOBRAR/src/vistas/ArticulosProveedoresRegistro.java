//------------------------- Procesar ---------------------------------------------------------    
    private void procesar(int op) {
       articuloproveedor=leerDatos();
        try {
            String msg=articuloproveedorcontroler.ArticuloProveedorProcesar(articuloproveedor, op);
            JOptionPane.showMessageDialog(null, msg);            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e.getMessage());
             System.out.println("Error"+e.getMessage());
        }       
   }
    
   private ArticuloProveedor leerDatos() {
        ArticuloProveedor artPro = new ArticuloProveedor();
        
        artPro.setDescripcion(txtArticulos.getText());
        artPro.setCampo1(campo1);
        artPro.setIdArticulosProveedores(idArtProve);   
             
        return artPro;
    }
   
   
   private void consultar(int id) throws Exception {
       articuloproveedor = articuloproveedorcontroler.ArticuloProveedorBuscar(id);
        if (articuloproveedor!= null) {

            labelIdArticulo.setText(articuloproveedor.getIdArticulosProveedores()+"");
            idArtProve = articuloproveedor.getIdArticulosProveedores();
            txtArticulos.setText(articuloproveedor.getDescripcion());
                       
        } else {
            JOptionPane.showMessageDialog(null, "Articulo no registrado");
            //System.out.println("Error");
        }
    }
   

   // ------------------------------ Listar Tabla ----------------------------------------
    
    void listaArticuloProveedor() {
        List<ArticuloProveedor> lista;
        try {
            
            lista = articuloproveedorcontroler.ArticuloProveedorListar();
            verArticuloProveedor(lista);

        } catch (Exception e) {
//            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println("ERROR: " + e.getMessage());
        }
    }

     private void verArticuloProveedor(List<ArticuloProveedor> lista) {

        DefaultTableModel tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        for (ArticuloProveedor artPro : lista) {
            Object[] fila = {artPro.getIdArticulosProveedores(), artPro.getDescripcion()};
            tabla.addRow(fila);
        }
    }
